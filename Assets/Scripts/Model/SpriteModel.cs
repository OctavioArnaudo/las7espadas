/// <summary>
/// 
/// Enum to represent the orientation of a sprite when it is displaced.
/// 
/// The orientation indicates how the sprite was drawn in relation to its displacement.
/// 
/// </summary>
public enum SpriteModel
{
    /// <summary>
    /// 
    /// The sprite was drawn with its left side aligned to the displacement direction.
    /// 
    /// </summary>
    Left,
    /// <summary>
    /// 
    /// The sprite was drawn with its right side aligned to the displacement direction.
    ///
    /// </summary>
    Centered,
    /// <summary>
    /// 
    /// The sprite was drawn with its right side aligned to the displacement direction.
    ///
    /// </summary>
    Right
}